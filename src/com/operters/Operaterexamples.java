package com.operters;

public class Operaterexamples {
	public static void main(String[] args) {
		int a=15;
		int b=20;
		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a/b="+(a/b));
		System.out.println("a%b="+(a%b));
	}

}
