package com.unary;

public class Unaryoperaterexample {
	public static void main(String[] args) {
		int i=10;
		--i;
		System.out.println(i);
		--i;;
		System.out.println(i);
		--i;
		System.out.println(i);
		--i;;
		System.out.println(i);
		i--;
		System.out.println(i);
		--i;;
		System.out.println(i);
		i--;
		System.out.println(i);
		i++;
		System.out.println(i);
		i--;
		
	}

}
